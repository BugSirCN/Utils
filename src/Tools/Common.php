<?php

namespace Bugsir\Utils\Tools;

class Common
{
    /**
     * 随机字符串
     * @param int $length
     * @return string
     */
    public function nonceStr($length = 16)
    {
        $chars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
        $str = "";
        for ($i = 0; $i < $length; $i++) {
            $str .= substr($chars, mt_rand(0, strlen($chars) - 1), 1);
        }
        return $str;
    }
}